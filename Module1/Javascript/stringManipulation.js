// concatination
let str1 = "Hello";
let str2 = "World";

let result = str1 + " " + str2;
console.log(result);

let result2 = str1.concat(" ", str2);
console.log(result2);

// substring
let str = "Hello, World!";
result = str.slice(7, 12);
console.log(result);

result2 = str.substring(7, 12);
console.log(result2);